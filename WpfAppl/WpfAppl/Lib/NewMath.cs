using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace WpfAppl.Lib
{
    class NewMath
    {
        public int AddInt(int a, int b)
        {
            return a + b;
        }

        public int MulInt(int a, int b)
        {
            return a * b;
        }
        public int DivInt(int a, int b)
        {
            return a / b;
        }
        public int MinusInt(int a, int b)
        {
            return a - b;
        }
    }
}
